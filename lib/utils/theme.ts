/**
 * Theme utility for creating consistent theme objects
 */
export interface ThemeColors {
  background: string;
  foreground: string;
  card: string;
  cardForeground: string;
  popover: string;
  popoverForeground: string;
  primary: string;
  primaryForeground: string;
  secondary: string;
  secondaryForeground: string;
  muted: string;
  mutedForeground: string;
  accent: string;
  accentForeground: string;
  border: string;
  input: string;
  ring: string;
}

export interface Theme {
  light: ThemeColors;
  dark: ThemeColors;
}

export function createTheme(theme: Theme): Theme {
  return theme;
}
